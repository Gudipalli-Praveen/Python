# increasing triangle star

n = 6
for i in range(n):
    for j in range(i+1):
        print("*",end=" ")
    print()

# decreasing triangle star

n = 5
for i in range(n):
    for j in range(i,n):
        print("*",end=" ")
    print()

# right side increasing triangle

n = 5
for i in range(n):
    for j in range(i,n):
        print(" ",end=" ")
    for j in range(i+1):
        print("*",end=" ")
    print()

